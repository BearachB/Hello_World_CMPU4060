user_int_1 = int(input("Please enter a first integer:"))
user_int_2 = int(input("Please enter a second integer:"))

if user_int_1 > user_int_2:
    print("Your first number is bigger than your second")
elif user_int_1 < user_int_2:
    print("Your first number is smaller than your second")
elif user_int_1 == user_int_2:
    print("Your first number is equal to your second")
